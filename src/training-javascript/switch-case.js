let fruta = "maçã";

switch (fruta) {
  case "maçã":
    console.log("Você escolheu uma maçã");
    break;
  case "uva":
    console.log("Você escolheu uma uva");
    break;
  case "laranja":
    console.log("Você escolheu uma laranja");
    break;
  default:
    console.log("Fruta não encontrada");
}
